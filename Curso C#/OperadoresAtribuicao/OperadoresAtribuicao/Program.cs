// Operadores de atribuição

// =, +=, -=, *=, /=, %=

// Exemplos com esses operadores

a = 10; // significa que, a váriavel a RECEBE 10.
a += 2; // a váriavel a RECEBE a + 2; 
// ou seja, o valor que já contia dentro da
// váriavel, mais 2. 
a -= 2; // a váriavel a RECEBE a - 2;
// ou seja, o valor que já contia dentro da
// váriavel, menos 2. 

a *= 3; // Pegar o valor que já contem dentro da variavel
// e multiplicar por 3.


